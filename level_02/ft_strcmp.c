#include <stdio.h>
#include <string.h>

int ft_strcmp(char *s1, char *s2)
{
	int i;
	i = 0;

	while (s1[i])
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (s1[i] - s2[i]);
}

int main()
{
char str1[] = "Tasmia";
char str2[] = "Rahman";

printf("%d\n", ft_strcmp(str1, str2));
printf("%d\n", strcmp(str1, str2));
return(0);
}
